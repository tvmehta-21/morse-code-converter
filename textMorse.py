def textToMorse():
    userInput=str(input("Please type your message: "))
    morseCode=""

    for alphabet in userInput:
        if alphabet == " ":
            morseCode+="  "
        else:
            alphabet=alphabet.lower()
            morseCode+=code[alphabet]
        morseCode+=" "

    return morseCode

